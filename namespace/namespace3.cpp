#include<iostream>
int main()
{
    int x=10;
    std::cout<<"god is great"<<std::endl;
    std::cout<<x<<std::endl;
    return 0;
}

/*
here 
:: -> Scope resolution operator
std  -> namespace
cout -> object
<<   -> insertion operator
" god  is great" -> string base index will be passed
endl; -> function
*/